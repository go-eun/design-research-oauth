    for pattern in UI_PATTERNS:
        components = [c.strip() for c in pattern.replace('/', ' ').split()]
        for component in components:
            if component in candidates:
                return pattern

    # 2.5. PATTERN_ALIASES 매칭 (예: "쿠폰" → "포인트/쿠폰혜택")
    for pattern, aliases in PATTERN_ALIASES.items():
        for alias in aliases:
            if alias in candidates:
                return pattern

    # 3. 유의어 정확 매칭 (candidates 순회)
    for pattern, synonyms in UI_PATTERN_SYNONYMS.items():
        for candidate in candidates:
            if any(s.lower() == candidate.lower() for s in synonyms):
                return pattern

    # 4. 유의어 토큰 단위 포함 매칭 (전체 쿼리 기준)
    query_tokens = set(q.lower().split())
    for pattern, synonyms in UI_PATTERN_SYNONYMS.items():
        for synonym in synonyms:
            synonym_tokens = set(synonym.lower().split())
            if synonym_tokens and synonym_tokens.issubset(query_tokens):
                return pattern

    # 5. substring 포함 매칭 (2자 이상, candidates 순회)
    for pattern, synonyms in UI_PATTERN_SYNONYMS.items():
        for synonym in synonyms:
            for candidate in candidates:
                if len(candidate) >= 2 and synonym in candidate:
                    return pattern

    return None

