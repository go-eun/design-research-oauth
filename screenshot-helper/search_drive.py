def get_drive_service():
    creds = Credentials.from_authorized_user_file(TOKEN_PATH, SCOPES)
    if not creds.valid:
        if creds.expired and creds.refresh_token:
            creds.refresh(Request())
            with open(TOKEN_PATH, 'w') as f:
                f.write(creds.to_json())
        else:
            raise Exception("token.json 만료. auth_test.py를 다시 실행해주세요.")
    return build('drive', 'v3', credentials=creds)

# ── JSON 파일 전체 수집 (재귀 탐색) ───────────────────────
def fetch_all_json_files(service, folder_id):
    """폴더 하위 전체를 재귀 탐색해 JSON 파일 ID 목록 반환"""
    json_files = []
    page_token = None

    # 현재 폴더의 JSON 파일 수집
    while True:
        resp = service.files().list(
            q=f"'{folder_id}' in parents and mimeType='application/json' and trashed=false",
            fields="nextPageToken, files(id, name)",
            supportsAllDrives=True,
            includeItemsFromAllDrives=True,
            pageToken=page_token,
            pageSize=1000,
        ).execute()
        json_files.extend(resp.get('files', []))
        page_token = resp.get('nextPageToken')
        if not page_token:
            break

    # 하위 폴더 재귀 탐색
    page_token = None
    while True:
        resp = service.files().list(
            q=f"'{folder_id}' in parents and mimeType='application/vnd.google-apps.folder' and trashed=false",
            fields="nextPageToken, files(id, name)",
            supportsAllDrives=True,
            includeItemsFromAllDrives=True,
            pageToken=page_token,
            pageSize=1000,
        ).execute()
        for subfolder in resp.get('files', []):
            json_files.extend(fetch_all_json_files(service, subfolder['id']))
        page_token = resp.get('nextPageToken')
        if not page_token:
            break

    return json_files

def fetch_all_metadata(service):
    json_files = fetch_all_json_files(service, DRIVE_ROOT)
    metadata = []
    for f in json_files:
        try:
            content = service.files().get_media(fileId=f['id'],
                                                supportsAllDrives=True).execute()
            data = json.loads(content.decode('utf-8'))
            metadata.append(data)
        except Exception:
            pass
    return metadata

# ── 수집 월 목록 ───────────────────────────────────────────
def get_available_months(metadata):
    """Drive에 수집된 월 목록을 최신순으로 반환"""
    months = sorted(
        set(m.get('collected_month') for m in metadata if m.get('collected_month')),
        reverse=True
    )
    return months

# ── 필터링 ─────────────────────────────────────────────────
def search(metadata, app=None, category=None, month=None, ui_pattern=None):
    results = []
    resolved_category = expand_category(category) if category else None

    # ui_pattern 복수 처리 (쉼표 구분)
    resolved_patterns = []
    if ui_pattern:
        for p in ui_pattern.split(','):
            resolved = expand_ui_pattern(p.strip())
            if resolved:
                resolved_patterns.append(resolved)

    for m in metadata:
        if app and app not in m.get('app', ''):
            continue
        if resolved_category and resolved_category != m.get('category', ''):
            continue
        if month and month != m.get('collected_month', ''):
            continue
        if resolved_patterns:
            m_patterns = m.get('ui_pattern', [])
            if not any(p in m_patterns for p in resolved_patterns):
                continue
        results.append(m)
    return results

# ── 결과 출력 ──────────────────────────────────────────────
def print_results(results):
    if not results:
        print("\n검색 결과가 없습니다.")
        return
    print(f"\n검색 결과: {len(results)}개\n")
    print("─" * 60)
    for i, m in enumerate(results, 1):
        ui_pattern = ', '.join(m.get('ui_pattern', []))
        print(f"[{i}] {m.get('app', '-')} / {ui_pattern} / {m.get('collected_month', '-')}")
        print(f"     {m.get('file', '-')}")
        if m.get('notes'):
            print(f"     메모: {m['notes']}")
    print("─" * 60)

