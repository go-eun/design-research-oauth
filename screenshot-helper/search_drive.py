# ── 메인 ───────────────────────────────────────────────────
def main():
    parser = argparse.ArgumentParser(description='Drive 스크린샷 검색')
    parser.add_argument('--app',         type=str, help='앱 이름')
    parser.add_argument('--category',    type=str, help='카테고리')
    parser.add_argument('--month',       type=str, help='수집월 (예: 2026.04)')
    parser.add_argument('--ui_pattern',  type=str, help='ui_pattern')
    parser.add_argument('--output-json', type=str, help='결과를 JSON 파일로 저장')
    parser.add_argument('--list-months', action='store_true', help='수집된 월 목록 출력')
    args = parser.parse_args()
    if args.list_months:
        service = get_drive_service()
        metadata = fetch_all_metadata(service)
        months = get_available_months(metadata)
        print(json.dumps(months, ensure_ascii=False))
        return

    # 인자 없으면 대화형 모드
    if not any(vars(args).values()):
        app, category, month, ui_pattern = interactive_mode()
    else:
        app, category, month = args.app, args.category, args.month
        ui_pattern           = args.ui_pattern

    print("\nDrive 연결 중...")
    service  = get_drive_service()
    print("메타데이터 수집 중...")
    metadata = fetch_all_metadata(service)
    print(f"총 {len(metadata)}개 파일 로드 완료")

    results = search(metadata, app=app, category=category, month=month,
                     ui_pattern=ui_pattern)
    print_results(results)

    if args.output_json:
        with open(args.output_json, 'w', encoding='utf-8') as f:
            json.dump(results, f, ensure_ascii=False, indent=2)
        print(f"\n결과 저장: {args.output_json}")

if __name__ == '__main__':
    main()
